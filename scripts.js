//Copy of dataset for filtering
let current_data_set = [...champions_data_set];


//function to sort displayed cards by champion's buy cost
function sortByCost(){
  current_data_set = current_data_set.sort((a,b) => a.cost - b.cost)
  showCards()
  
}

//reset function to clear filters on cards
function reset(){
  current_data_set = [...champions_data_set]
  showCards()
  
}

// This function adds cards the page to display the data in the array
function showCards() {
  const cardContainer = document.getElementById("card-container");
  cardContainer.innerHTML = "";
  const templateCard = document.querySelector(".card");

  current_data_set.forEach((champion) => {
    const nextCard = templateCard.cloneNode(true)
    editCardContent(nextCard, 
                    champion.name, 
                    champion.image, 
                    champion.traits, 
                    champion.cost, 
                    champion.type)

    cardContainer.appendChild(nextCard)
  })
}


// This function creates the card's layout using data passed in
function editCardContent(card, newTitle, newImageURL, traits, cost, type) {
  card.style.display = "block";

  const cardHeader = card.querySelector("h1");
  let color;
  if(cost == 2){
    color = 'green'
  }
  else if(cost == 3){
    color = 'blue'
  }
  else if(cost == 4){
    color = 'purple'
  }
  else if(cost == 5){
    color = 'yellow'
  }
    
  cardHeader.textContent = newTitle;
  cardHeader.style.color = color;
  card.style.boxShadow = `0 20px 15px ${color}`;

  const cardImage = card.querySelector("img");
  cardImage.src = newImageURL;
  cardImage.alt = newTitle + " Poster";

  const ul = card.querySelector("ul");
  //Clear the ul element first
  ul.innerHTML = `
    <li>Cost: ${cost}</li>
    <li>Type: ${type}</li>
    <li>Traits: ${traits.join(", ")}</li>
  `;

  console.log("new card:", newTitle, "- html: ", card);
}

/**
 * this function will let users search for champions, then create those champion's cards
 * as search results
 * Cards are clickable to add to team builder
 */
function displaySearch(result){
  const searchResult = document.getElementById('search-result')
  searchResult.innerHTML = ""

  const templateCard = document.querySelector(".card");

  result.forEach((champion) => {
    const nextCard = templateCard.cloneNode(true)
    editCardContent(nextCard, 
                    champion.name, 
                    champion.image, 
                    champion.traits, 
                    champion.cost, 
                    champion.type)

    nextCard.addEventListener('click', () => addToTeam(champion))
    searchResult.appendChild(nextCard)

  })

}

//function to add champions to team builder
function addToTeam(champion){
  if (team.length >= 9){
    alert('Team is full')
    return
  }

  if (team.find(champ => champ.name == champion.name)){
    alert(`Champion is already in the team!`)
    return
  }
  
  team.push(champion)
  updateTeamDisplay()
}

// Function to remove cards from team builder
function removeFromTeam(champion){
  //make a new team without the champion
  team = team.filter(c => c.name !== champion.name)
  updateTeamDisplay()
}

// function to update team builder display every time we add and remove card
// from builder
function updateTeamDisplay(){
  const teamSlot = document.getElementById('team-slot')
  teamSlot.innerHTML = "";

  const templateCard = document.querySelector(".card");

  team.forEach((champion) => {
    const nextCard = templateCard.cloneNode(true)
    editCardContent(nextCard, 
                    champion.name, 
                    champion.image, 
                    champion.traits, 
                    champion.cost, 
                    champion.type)

    
    nextCard.addEventListener('click', () => removeFromTeam(champion))
    teamSlot.appendChild(nextCard);
    updateTraits()
  })
}


// Function to select selected champions' traits and add them to a counter
function updateTraits(){
  const traitDisplay = document.querySelector('#right-panel h1');
  const traitCounter = {}

  team.forEach((champ) => {
    champ.traits.forEach((trait) => {
      //check if trait exists in team, if yes add 1 to counter
      //For each loop does this for each trait
      if(traitCounter[trait]){
        traitCounter[trait] = traitCounter[trait] + 1;
      }
      else{
        //if trait doesn't exist, create the trait and start it at 1
        traitCounter[trait] = 1;
      }

      //get key value out of trait counter object
      const traitString = Object.entries(traitCounter)
      .map(([trait,num]) => {
        //depending on trait count, will change text color
        const color = getTraitColors(num);
        return `${trait}: <span style="color: ${color}">${num} </span>`;
        })//.map runs once every pair to turn it into a string
        .join(' | ') 
      
      
      if(traitString == ''){
        traitDisplay.innerHTML = 'Active Traits: '
      }
      else{
        traitDisplay.innerHTML = `Active Traits: ${traitString}`
      }
    })
  })


}

//get different colors to highlight trait combos
function getTraitColors(num){
  if(num == 1) {return 'gray'}
  if(num == 2){ return 'lightblue' }
  if(num == 3){ return 'green'}
  if(num == 4){ return 'purple'}
  if(num > 4) { return 'gold'}
  
} 

// This calls the addCards() function when the page is first loaded
document.addEventListener("DOMContentLoaded", function(){
  const filterToggle = document.getElementById("filter-toggle");
  const panel = document.getElementById("filter-panel")
  const apply = document.getElementById("apply-filters")

  filterToggle.addEventListener('click', () =>{
    panel.classList.toggle('hidden')
    apply.classList.toggle('hidden')
  });

  
  // Checks for filters
  apply.addEventListener('click', ()=> {  
    const costValue = document.getElementById('cost-select').value
    const traitValue = document.getElementById('trait-select').value
    const typeValue = document.getElementById('type-select').value

    current_data_set = [...champions_data_set]

    if(costValue !== ''){
      current_data_set = current_data_set.filter(champ => champ.cost == costValue);
    }

    //for trait, we need to use includes since champions have multiple traits and is stored 
    //in an array
    if(traitValue !== ''){
      current_data_set = current_data_set.filter(champ => champ.traits.includes(traitValue));
    }


    if(typeValue !== ''){
      current_data_set = current_data_set.filter(champ => champ.type == typeValue);
    }


    showCards()
  })  

  const searchBar = document.getElementById('search-bar')
  searchBar.addEventListener('input', ()=>{
    
    const search = searchBar.value.toLowerCase()

    if (search === ''){
      document.getElementById('search-result').innerHTML = ""
      return
    }
    const result = champions_data_set.filter(champ => champ.name.toLowerCase().includes(search))

    displaySearch(result)
  })

  showCards()

});
